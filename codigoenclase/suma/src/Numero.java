public class Numero {
    // atributos
    private int numero;

    // constructores
    public Numero() {
    }

    public Numero(int numero) {
        this.numero = numero;
    }

    // get y set
    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }
}